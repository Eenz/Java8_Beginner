package by.schildt.chapter_1;

public class Example3 {
    public static void main(String[] args) {
        int var;
        double x;

        var = 10;
        x = 10.0;

        System.out.println("начальное значение var: " + var);
        System.out.println("начальное значение х: " + x);

        var = var / 4;
        x = x / 4;

        System.out.println("значение var после деления: " + var);
        System.out.println("значение х после деления: " + x);
    }
}
